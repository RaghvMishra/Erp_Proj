angular.module('hiworkApp')
    .factory('translatorService', ['$interpolate', 'AppStorage', '$window', 'sessionFactory','ajaxService', function ($interpolate, AppStorage, $window, sessionFactory, ajaxService) {
        var currentLanguage;
        var dictionary = $.extend(true, {}, $dictionary);
      
        return {
            setCurrentLanguage: function (selectLanguage) {
                sessionFactory.SetData(AppStorage.currentLanguage,selectLanguage)
                currentLanguage = sessionFactory.GetData(AppStorage.currentLanguage);
                $window.location.reload();
            },
            getCurrentLanguage: function () {
                return currentLanguage;
            },
          
            translator: function (phraseKey, params) {
                currentLanguage = sessionFactory.GetData(AppStorage.currentLanguage);
                if (!currentLanguage || angular.isUndefined(currentLanguage))
                    currentLanguage = 'en';
                if (params === null || $.isEmptyObject(params)) {
                    return dictionary[currentLanguage][phraseKey];
                } else {
                    return $interpolate(dictionary[currentLanguage][phraseKey])(params);
                }
            }
        };
    }])
    .filter('translator', ['translatorService', function (translatorService) {
        return function (phraseKey, params) {
            return translatorService.translator(phraseKey, params);
        };
    }])